public class DivisionEntera {
    public static void main(String[] args) {

        // Este programa hace la siguiente operacion: 
        // (7/2)*4 y el resultado tiene que ser 14

        int dividendo = 7;
        float divisor = 2;
        float cociente = dividendo/divisor;

        float multiplicando = 4;

        float resultadoFinal = cociente * multiplicando;

        System.out.println((int)resultadoFinal);
    }
}
